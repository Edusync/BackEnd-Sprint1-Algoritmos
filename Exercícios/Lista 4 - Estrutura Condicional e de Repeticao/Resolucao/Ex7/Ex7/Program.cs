using System;

namespace Ex7
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Exercício 7 - Lista 4

            //Usamos a estrutura de repetição (For)
            //Definimos um intervalo de 1 a 10
            //Cada vez que for repetir ele acrescenta mais um (i++)
            for(int i = 1; i <= 10; i++)
            {
                //Escreve o valor do índice a cada repetição
                Console.WriteLine(i);
            }
        }
    }
}
